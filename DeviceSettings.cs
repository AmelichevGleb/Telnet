using System;

public class DeviceSettings
{
    
    public string port
    {
        get { return port; }
        set { port = value; }
    } 

    public string speed
    {
        get { return speed; }
        set { speed = value; }
    }
    public string countBit
    {
        get { return countBit; }
        set { countBit = value; }
    }
    public string control
    {
        get { return control; }
        set { control = value; }
    }
    public string countStopBit
    {
        get { return countStopBit; }
        set { countStopBit = value; }
    }
}